def huffman_encode(symbol, table):
    code, length = table[symbol]
    return code, length

def huffman_decode(bit_reader, table):
    bits = 0
    bit_len = 0
    inv_table = {(code, length): symbol for symbol, (code, length) in table.items()}
    while True:
        bits = (bits << 1) | bit_reader.read_bit()
        bit_len += 1
        if (bits, bit_len) in inv_table:
            return inv_table[(bits, bit_len)]