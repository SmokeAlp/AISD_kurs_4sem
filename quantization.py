def scale_quantization_table(q_table, quality):
    if quality >= 50:
        S = 200 - 2 * quality
    else:
        S = 5000.0 / quality
    new_q = np.ceil(q_table * S / 100.0)
    new_q = np.clip(new_q, 1, None)
    return new_q.astype(np.uint8)

def quantize(block, q_table):
    return np.round(block / q_table).astype(np.int32)

def dequantize(block, q_table):
    return (block * q_table).astype(np.float64)